
public class Datatype {

	public static void main(String[] args) {
		int num = 20;
		double deci = 2.45656;
		boolean yes = true;
		char ch = 'a';
		String string = "string";
		
		System.out.println("Number: " + num 
				+ " \nDecimal:  " + deci 
				+ " \nCharacter:  " + ch 
				+ " \nString:  " + string 
				+ " \nBoolean:  " + yes);
	}

}
